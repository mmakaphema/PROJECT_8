#TUPLES

my_tuple =(1, 2, 3, 4, 5)
print(my_tuple)
print(my_tuple[0])
print(my_tuple[3])
print(my_tuple[-1])

#Concatenation
#ADDS UP DIFFERENT SETS OF TUPLES
tuple1 = (1, 2, 3, 4, 5)
tuple2 = (6, 7, 8, 9, 10)
concat_tuple = tuple1 + tuple2
print(concat_tuple)

#REPETITION
#REPEATS THE SAME SET OF A TUPLE
rep_tuple = tuple1 * 4
print(rep_tuple)